function getPersonaStrings(lang, personaId) {
  const code = (lang || '').split('-')[0];
  const byLang = PERSONA_I18N[code] || PERSONA_I18N.en;
  const entry = byLang[personaId] || PERSONA_I18N.en[personaId] || PERSONA_I18N.en.default;
  const name = entry?.name || personaId;
  const desc = entry?.desc || '';
  return { name, desc };
}

function getPersonaLabel(lang, persona) {
  if (!persona) return 'Unknown';
  const { name } = getPersonaStrings(lang, persona.id || persona);
  return name || persona.nameEn || persona.name || persona;
}

module.exports = { PERSONA_I18N, getPersonaStrings, getPersonaLabel };
